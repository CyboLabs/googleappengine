package com.google.appengine.tools.admin;

/**
 * Exception thrown when the VerificationCodeReceiver could not get a RedirectUri.
 *
 */
public class VerificationCodeReceiverRedirectUriException extends Exception {
  public VerificationCodeReceiverRedirectUriException(String message, Throwable cause) {
    super(message, cause);
  }

  public VerificationCodeReceiverRedirectUriException(String message) {
    super(message);
  }

  public VerificationCodeReceiverRedirectUriException() {
    super();
  }
}
